package petljaWhile;

public class Primer2 {

	public static void main(String[] args) {
		// while i ++ u uslovnom delu
		int i = 0;
		System.out.print("++i\t:");
		while(++i < 5) 
			System.out.print(i + " ");
		
		i = 0;
		System.out.print("\ni++\t:");
		while(i++ < 5) 
			System.out.print(i + " ");

	}

}
