package petljaFor;

public class Main {
	
	public static void main(String[] args) {
		
		// ispisivanje 0,1,2,...,8,9
		
		for(int cifra = 0; cifra < 10; cifra++) {
			System.out.println(cifra);
		}
		
		// drugi način
		for(int cifra = 9; cifra >= 0; cifra--) {
			System.out.println(cifra);
		}

	}
	
	

}
